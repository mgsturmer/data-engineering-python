#Operadores de Associação em Python

curso = "Curso de Python"
frutas = ["Laranja", "Uva", "Limão"]
saques = [1500, 100]

#A Palavra Python está na variavel curso
print("Python" in curso)
#True

#A Fruta "Maçã" não esta na lista de frutas
print("Maçã" not in frutas)
#false

#Existe um saque de 200?
print(200 in saques)
#false