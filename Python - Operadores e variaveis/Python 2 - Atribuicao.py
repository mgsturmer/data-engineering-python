#Atribuição de valores a variaveis.

#variavel e valor
saldo = 500
deposito = 200
saque = 10

print(saldo)
#500

saldo_atualizado = saldo + deposito
print(saldo_atualizado)
#700

#Outras maneiras de atribuir

saldo += deposito
print(saldo)

saldo -= saque
print(saldo)

saldo /= 50
print(saldo)

saldo //= 50
print(saldo)

saldo *= 2
print(saldo)